/* Itinerario compartido (localStorage) */
window.ZANATE_ITINERARIO = {
  clave: "zanate_itinerario",
  obtener() {
    try { return JSON.parse(localStorage.getItem(this.clave)) || []; } catch { return []; }
  },
  guardar(lista) {
    localStorage.setItem(this.clave, JSON.stringify(lista));
    document.dispatchEvent(new CustomEvent("itinerario:cambio", { detail: lista }));
  },
  alternar(id) {
    const lista = this.obtener();
    const i = lista.indexOf(id);
    if (i >= 0) lista.splice(i, 1); else lista.push(id);
    this.guardar(lista);
    return lista.includes(id);
  },
  limpiar() { this.guardar([]); }
};
